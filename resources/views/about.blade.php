        <h2>Formations & Formateurs</h2>
        <p>Le Rio Va’a Club offre un soutien à tous ceux qui veulent participer à ce sport en tant qu’athlète médaillé.</p>

        <p>L’entraînement pour les compétitions par équipe demande à chacun de se donner au groupe.</p>

        <p>Quiconque veut concourir en équipe doit garder à l’esprit qu’il n’est qu’un collaborateur parmi d’autres, même s’il est le meilleur athlète au monde dans le sport. Il doit abandonner sa volonté de faire ce que tout le monde veut. Il peut arriver que le jour le plus approprié pour que toute l’équipe s’entraîne soit celui que vous vous étiez réservé. Vous abandonnez donc votre projet privé pour ne pas détourner l’équipe.</p>

