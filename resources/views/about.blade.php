        <h2>Pagayons sur les vagues de la mer</h2>

        <p>L'objectif principal du club est de promouvoir la pratique du Va’a, dans le respect de la tradition polynésienne. Nous proposons deux modèles de pirogues va’a : la pirogue hawaïenne, la plus répandue dans le pays, connue sous le nom de « pirogue hawaïenne », et la pirogue tahitienne, notre nouveauté dans la ville de Rio de Janeiro.</p>

        <p>Le Va’a River Club propose à la fois du canotage récréatif, juste pour le plaisir, et du canoë de compétition. Vous pouvez choisir de participer à l’un ou aux deux.</p>

        <p>Nous formons des équipes compétitives composées de femmes et d'hommes, ainsi que de para-athlètes.</p>

        <h3>Voulez-vous perdre du poids ou gagner du muscle grâce au sport ?</h3>
        <p>Participez aux compétitions en tant qu'athlète du Rio Va’a Club. Avec la discipline de l’entraînement, nous vous aiderons à atteindre ces objectifs.</p>

        <h3>Jours et horaires de pagaie libre :</h3>
        <p>mardi et jeudi à 6 h, 8 h et 18 h. Samedi à 7 h. En face du restaurant Garota da Urca.</p>

        <p>Les séances d’entraînement de l’équipe sont programmées individuellement à d’autres moments ou jours.</p>

        <h2>Historique</h2>
        <p>Le Va’a River Club (ou Outrigger Rio Club – Va’a Hui Hoe River) a été le premier club de Va’a (canoë polynésien) fondé en Amérique latine, ayant commencé ses activités en novembre 2000 avec l’arrivée dans le pays de la pirogue Lanakila, importée de Californie par Ronald Williams et Sérgio Koeningsfeld. Légalement, il a été officialisé en 2003, avec un statut enregistré dans une étude notariale, ainsi qu’un enregistrement CNPJ et municipal.</p>

        <p>Le club a été affilié à plusieurs entités représentant le sport, que ce soit dans l’État, dans le pays ou à l’international. Il a rejoint la Fédération internationale de va’a (FIV) en 2003, la Fédération de canoë-kayak de l’État de Rio de Janeiro (Fecaerj) en 2009, la Fédération hawaïenne de canoë de l’État de Rio de Janeiro (FCHERJ) en 2010 et la Confédération brésilienne de va’a (CBVa’a) en 2018.</p>

